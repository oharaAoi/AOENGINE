#include "BossStateNormal.h"
#include "Game/Actor/Boss/Boss.h"

///////////////////////////////////////////////////////////////////////////////////////////////
// ↓ 開始処理
///////////////////////////////////////////////////////////////////////////////////////////////

void BossStateNormal::OnStart() {
	SetName("NormalState");
	floatingTween_.Init(-0.5f, 0.5f, 1.5f, (int)EasingType::InOut::Sine, LoopType:: Return);
}

///////////////////////////////////////////////////////////////////////////////////////////////
// ↓ 更新処理
///////////////////////////////////////////////////////////////////////////////////////////////

void BossStateNormal::OnUpdate() {
	Math::Vector3 pos = pOwner_->GetTransform()->GetTemporaryTranslate();
	floatingTween_.Update(AOENGINE::GameTimer::DeltaTime());
	pos.y += floatingTween_.GetValue();
	pOwner_->GetTransform()->SetTemporaryTranslate(pos);
}

///////////////////////////////////////////////////////////////////////////////////////////////
// ↓ 終了処理
///////////////////////////////////////////////////////////////////////////////////////////////

void BossStateNormal::OnExit() {
}

void BossStateNormal::Debug_Gui() {
}
