#include "BossActionBackSweep.h"
#include "Game/Actor/Boss/Boss.h"
#include "Engine/Lib/Math/Easing.h"

///////////////////////////////////////////////////////////////////////////////////////////////
// ↓ コンストラクタ
///////////////////////////////////////////////////////////////////////////////////////////////

BossActionBackSweep::BossActionBackSweep() {
	param_.Load();
}

///////////////////////////////////////////////////////////////////////////////////////////////
// ↓ 実行処理
///////////////////////////////////////////////////////////////////////////////////////////////

BehaviorStatus BossActionBackSweep::Execute() {
	return Action();
}

///////////////////////////////////////////////////////////////////////////////////////////////
// ↓ 重みの計算
///////////////////////////////////////////////////////////////////////////////////////////////

float BossActionBackSweep::EvaluateWeight() {
	return 0.5f;
}

///////////////////////////////////////////////////////////////////////////////////////////////
// ↓ 編集処理
///////////////////////////////////////////////////////////////////////////////////////////////

void BossActionBackSweep::Debug_Gui() {
	BaseTaskNode::Debug_Gui();
	param_.Debug_Gui();
}

void BossActionBackSweep::Parameter::Debug_Gui() {
	ImGui::DragFloat("threshold", &threshold, 0.1f);
	ImGui::DragFloat("rotateTime", &rotateTime, 0.1f);
	Math::SelectEasing(easeType, "##BossActionBackSweep");
	SaveAndLoad();
}

///////////////////////////////////////////////////////////////////////////////////////////////
// ↓ 終了確認
///////////////////////////////////////////////////////////////////////////////////////////////

bool BossActionBackSweep::IsFinish() {
	if (taskTimer_ > param_.rotateTime) {
		return true;
	}
	return false;
}

///////////////////////////////////////////////////////////////////////////////////////////////
// ↓ 実行確認
///////////////////////////////////////////////////////////////////////////////////////////////

bool BossActionBackSweep::CanExecute() {
	Math::Vector3 forward = pTarget_->GetTransform()->GetRotate().MakeForward();
	Math::Vector3 toPlayer = (pTarget_->GetTargetPos() - pTarget_->GetPosition()).Normalize();
	float dot = Dot(forward, toPlayer);
	if (dot < param_.threshold) {
		return true;
	}

	return false;
}

///////////////////////////////////////////////////////////////////////////////////////////////
// ↓ 初期化処理
///////////////////////////////////////////////////////////////////////////////////////////////

void BossActionBackSweep::Init() {
	pTarget_->SetIsAttack(true);
	pTarget_->GetFlamethrowers()->SetAttackType(FlamethrowerAttackType::Sword);

	// 回転を求める
	AOENGINE::WorldTransform* transform = pTarget_->GetTransform();
	startRotate_ = transform->GetRotate();

	// playerの方向を向く回転を求める
	Math::Vector3 dir = (pTarget_->GetTargetPos() - pTarget_->GetPosition()).Normalize();
	endRotate_ = Math::Quaternion::LookAt(startRotate_.MakeForward(), dir);
}

///////////////////////////////////////////////////////////////////////////////////////////////
// ↓ 更新処理
///////////////////////////////////////////////////////////////////////////////////////////////

void BossActionBackSweep::Update() {
	if (!pTarget_->GetFlamethrowers()->Deploy()) {
		return;
	}
	taskTimer_ += AOENGINE::GameTimer::DeltaTime();

	Sweep();
}

///////////////////////////////////////////////////////////////////////////////////////////////
// ↓ 終了処理
///////////////////////////////////////////////////////////////////////////////////////////////

void BossActionBackSweep::End() {
	pTarget_->GetFlamethrowers()->Remove();
}

///////////////////////////////////////////////////////////////////////////////////////////////
// ↓ 薙ぎ払い処理
///////////////////////////////////////////////////////////////////////////////////////////////

void BossActionBackSweep::Sweep() {
	float t = taskTimer_ / param_.rotateTime;
	Math::Quaternion rotate = Math::Quaternion::Slerp(startRotate_, endRotate_, Math::CallEasing(param_.easeType, t));
	pTarget_->GetTransform()->SetRotate(rotate);
}
