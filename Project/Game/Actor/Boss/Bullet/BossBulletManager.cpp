#include "BossBulletManager.h"

BossBulletManager::~BossBulletManager() {
	bulletList_.clear();
}

///////////////////////////////////////////////////////////////////////////////////////////////
// ↓ 初期化する
///////////////////////////////////////////////////////////////////////////////////////////////

void BossBulletManager::Init() {
	bulletList_.clear();
}

///////////////////////////////////////////////////////////////////////////////////////////////
// ↓ 更新する
///////////////////////////////////////////////////////////////////////////////////////////////

void BossBulletManager::Update() {
	CheckBulletLife();

	for (auto& bullet : bulletList_) {
		bullet->SetTargetPosition(playerPosition_);
		bullet->Update();
	}
}
