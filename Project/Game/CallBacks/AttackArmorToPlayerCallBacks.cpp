#include "AttackArmorToPlayerCallBacks.h"
#include "Game/Information/ColliderCategory.h"

///////////////////////////////////////////////////////////////////////////////////////////////
// ↓ 初期化処理
///////////////////////////////////////////////////////////////////////////////////////////////

void AttackArmorToPlayerCallBacks::Init() {
	SetCallBacks();
	SetPair(pCollisionManager_, ColliderTags::Boss::attackArmor, ColliderTags::Player::own);
}

///////////////////////////////////////////////////////////////////////////////////////////////
// ↓ 更新処理
///////////////////////////////////////////////////////////////////////////////////////////////

void AttackArmorToPlayerCallBacks::Update() {

}

///////////////////////////////////////////////////////////////////////////////////////////////
// ↓ 衝突開始時
///////////////////////////////////////////////////////////////////////////////////////////////

void AttackArmorToPlayerCallBacks::CollisionEnter([[maybe_unused]] AOENGINE::BaseCollider* const armor, [[maybe_unused]] AOENGINE::BaseCollider* const player) {
	pPlayer_->Damage(pBoss_->GetAttackArmorDamage());
}

///////////////////////////////////////////////////////////////////////////////////////////////
// ↓ 衝突中
///////////////////////////////////////////////////////////////////////////////////////////////

void AttackArmorToPlayerCallBacks::CollisionStay([[maybe_unused]] AOENGINE::BaseCollider* const armor, [[maybe_unused]] AOENGINE::BaseCollider* const player) {
}

///////////////////////////////////////////////////////////////////////////////////////////////
// ↓ 衝突終了時
///////////////////////////////////////////////////////////////////////////////////////////////

void AttackArmorToPlayerCallBacks::CollisionExit([[maybe_unused]] AOENGINE::BaseCollider* const armor, [[maybe_unused]] AOENGINE::BaseCollider* const player) {
}
