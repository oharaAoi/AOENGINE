#include "Enemy.h"

Enemy::Enemy() {
}

Enemy::~Enemy() {
}

void Enemy::Finalize() {
}
//////////////////////////////////////////////////////////////////////////////////////////////////
// ↓　初期化処理
//////////////////////////////////////////////////////////////////////////////////////////////////

void Enemy::Init() {
	BaseGameObject::Init();
	SetObject("star.obj");

	isAlive_ = true;
	tag_ = "enemy";
	radius_ = 2.0f;
}

//////////////////////////////////////////////////////////////////////////////////////////////////
// ↓　更新処理
//////////////////////////////////////////////////////////////////////////////////////////////////

void Enemy::Update() {
	BaseGameObject::Update();
}

//////////////////////////////////////////////////////////////////////////////////////////////////
// ↓　描画処理
//////////////////////////////////////////////////////////////////////////////////////////////////

void Enemy::Draw() const {
	BaseGameObject::Draw();
}

void Enemy::OnCollision(Collider* other) {
	if (other->GetTag() == "playerBullet") {
		isAlive_ = false;
	}
}

const Vector3 Enemy::GetWorldPos() const {
	return Transform(Vector3::ZERO(), transform_->GetWorldMatrix());
}
