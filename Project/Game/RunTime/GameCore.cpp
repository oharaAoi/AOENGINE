#include "GameCore.h"
#include "Engine/Engine.h"

GameCore::~GameCore() {}

//////////////////////////////////////////////////////////////////////////////////////////////////
// ↓　終了処理
//////////////////////////////////////////////////////////////////////////////////////////////////

void GameCore::Finalize() {
	sceneManger_->Finalize();
	AOENGINE::AoiFramework::Finalize();
}

//////////////////////////////////////////////////////////////////////////////////////////////////
// ↓　初期化処理
//////////////////////////////////////////////////////////////////////////////////////////////////
void GameCore::Init() {
	AOENGINE::AoiFramework::Init();

	// シーンの初期化
	sceneManger_ = std::make_unique<AOENGINE::SceneManager>();
	sceneManger_->Init();
	sceneManger_->SetChange(SceneType::Title);

	isReset_ = false;
}

//////////////////////////////////////////////////////////////////////////////////////////////////
// ↓　更新処理
//////////////////////////////////////////////////////////////////////////////////////////////////

void GameCore::Update() {
	profiler_.Start();
	// sceneの更新
	sceneManger_->Update();
	profiler_.End("Update");
}

//////////////////////////////////////////////////////////////////////////////////////////////////
// ↓　描画処理
//////////////////////////////////////////////////////////////////////////////////////////////////

void GameCore::Draw() {
	profiler_.Start();
	sceneManger_->Draw();
	profiler_.End("Draw");
}

//////////////////////////////////////////////////////////////////////////////////////////////////
// ↓　frame開始処理
//////////////////////////////////////////////////////////////////////////////////////////////////

void GameCore::BeginFrame() {
	profiler_.Start();
	Engine::BeginFrame();
	profiler_.End("BeginFrame");
}

//////////////////////////////////////////////////////////////////////////////////////////////////
// ↓　frame終了処理
//////////////////////////////////////////////////////////////////////////////////////////////////

void GameCore::EndFrame() {
	profiler_.Start();

	// frameの最終処理を行う
	Engine::RenderFrame();
	profiler_.End("EndFrame");
#ifdef _DEBUG
	profiler_.DisPlay();
#endif

	Engine::EndFrame();
	// fpsの計算
	gameTimer_.CalculationFrame();
}