#include "EnergyOutput.h"

///////////////////////////////////////////////////////////////////////////////////////////////
// ↓ 初期化処理
///////////////////////////////////////////////////////////////////////////////////////////////

void EnergyOutput::Init(const std::string& _groupName, const std::string& _itemName) {
	SetName("EnergyOutput");
	BaseGaugeUI::Init("gauge_bg.png", "gauge_front.png");
	front_->Load(_groupName, _itemName);
}

///////////////////////////////////////////////////////////////////////////////////////////////
// ↓ 更新処理
///////////////////////////////////////////////////////////////////////////////////////////////

void EnergyOutput::Update(float _fillAmount) {
	fillAmount_ = _fillAmount;
	BaseGaugeUI::Update();
}

///////////////////////////////////////////////////////////////////////////////////////////////
// ↓ 編集処理
///////////////////////////////////////////////////////////////////////////////////////////////

void EnergyOutput::Debug_Gui() {
	BaseGaugeUI::Debug_Gui();
}