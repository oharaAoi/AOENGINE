#include "BaseBulletManager.h"

///////////////////////////////////////////////////////////////////////////////////////////////
// ↓ ポインタに対するColliderの探索
///////////////////////////////////////////////////////////////////////////////////////////////

BaseBullet* BaseBulletManager::SearchCollider(AOENGINE::BaseCollider* collider) {
	for (std::unique_ptr<BaseBullet>& bullet : bulletList_) {
		if (bullet->GetCollider() == collider) {
			return bullet.get();
		}
	}

	return nullptr;
}

///////////////////////////////////////////////////////////////////////////////////////////////
// ↓ Bulletの生存確認
///////////////////////////////////////////////////////////////////////////////////////////////

void BaseBulletManager::CheckBulletLife() {
	std::erase_if(bulletList_, [](const std::unique_ptr<BaseBullet>& bullet) {
		return !bullet->GetIsAlive();
				  });
}
